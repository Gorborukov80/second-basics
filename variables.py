# Описание сотрудника
# Имя(тип строка)
co_worker = 'Max'
print(co_worker, type(co_worker))

# Возраст (тип целое чило)
age = 20
print(age, type(age))

# Профессионал или нет (логический тип)
is_professional = True
print(is_professional, type(is_professional))

# Средний уровень профессионализма сотрудника (число с плавающей точкой)
avg_level = 4.5
print(avg_level, type(avg_level))
